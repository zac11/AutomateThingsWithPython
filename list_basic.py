#simple list
spam=['messi','ronaldo','costa','ibra']
print(spam[1:4])
print(spam[:4])
print(spam[:-1])
print(spam[-1:])
print(spam[3:])

#list within a list
spam2=[['cat','bat'],['hat','sat']]
print(spam2[1][1])
print(spam2[-1][0])
print(spam2[0][-1])
print(spam2[-1][-1])

#length of a list
print(len(spam2))
print(len(spam))