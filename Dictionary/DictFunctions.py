dictEuro={'country':'France','finalists':'Portugal and France','highest-scorer':'Greizemann'}

"""
Printing all the values in the dictionaries
"""
for value in dictEuro.values():
    print(value)

"""
Printing all the keys in the dictionaries
"""
for keys in dictEuro.keys():
    print(keys)

"""
Printing all the items- key value pairs in the dictionary
"""
for item in dictEuro.items():
    print(item)